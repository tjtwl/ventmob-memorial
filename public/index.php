	<section class="timeline">
		<div class="wrapper">
			<h2 class="timeline">
				<small>(2008 - 2011)</small> The mob years
			</h2>
			<p>
				Below you can view a timeline of the VentMob history. If you get the feeling something is missing, be sure to <a href="https://github.com/thejjokerr/ventmob-memorial/issues" target="_blank">post an issue on GitHub</a> with the missing event or information and we'll correct it as soon as possible.
			</p>
			<ul class="timeline">
				<li class="important icon-start">
					<div class="line"> 
						<small>27th November 2008</small>
						<h3>Start</h3>
						<p>
							VentMob was founded by Clown, KiZai, Drewley, Wyvern, Chaz and Dank. The goal was to create a laid back or 'semi-serious' roleplaying server which were scarce at the time.
						</p>
						<p>
							The name 'VentMob' stems from the communications software 'Ventrilo' which the originals founders used to communicate.
						</p>
						<p>
							The semi-serious roleplaying gamemode was a modified version of Cider (by <a href="http://conna.org/" target="_blank">Conna Wiles</a>). The map which the players used to roleplay on was originally rp_tb_city45_v01 (created by the Taco N' Banana community).
						</p>
					</div>
				</li>
				<li class="icon-vehicle">
					<div class="line">
						<small>Early 2009</small>
						<h3>Lexi joins</h3>
						<p>
							Joining in on the fun early, <a href="http://lexi.org.uk/">Lexi</a> joins the forums and shows the community a boat-vehicle he is working on. Little did we know this coder would become the best thing to happen to VentMob.
						</p>
						<p>
							Lexi overhauled, optimized and improved the Cider script and called it 'Applejack' (as a pun on Cider). With these changes the popularity of the VentMob gaming community skyrocketed and the community grew larger.
						</p>
						<p>
							Around this time the map was changed to rp_evocity_v2d to allow Lexi's vehicle system to be used. Soon people were happily buying cars and racing through EvoCity.
						</p>
					</div>
				</li>
				<li class="icon-dog">
					<div class="line">
						<small>July 2009</small>
						<h3>The Experiment</h3>
						<p>
							Under the slogan <i>"It's a dog eat dog world out there and these dogs have guns"</i> a new gamemode was launched in the summer of 2009. The Experiment script by Conna Wiles was obtained and modified to fit the VentMob community.
						</p>
						<p>
							VentMob's version of Experiment was unique and very popular, causing once more for an influx of new players in the community.
						</p>
					</div>
				</li>
				<li class="icon-radio">
					<div class="line">
						<small>Early 2010</small>
						<h3>VentMob Radio</h3>
						<p>
							For a while VentMob launched it's own internet radio station. With several of the admins and members contributing to different time-slots.
						</p>
						<p>
							A popular show on VentMob Radio was the 'Syff and Mori Show' where the two DJ's discussed topics and played chip-tune music whilst being physically separated by the Atlantic Ocean.
						</p>
					</div>
				</li>
